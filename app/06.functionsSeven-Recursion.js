// Solution via recursion:
function findIndex2(s, c) {
  function check(index) {
    // базовий випадок: якщо індекс за межами рядка
    if (index >= s.length) return -1;

    // якщо символ збігся → повертаємо індекс
    if (s[index] === c) return index;

    // інакше перевіряємо наступний індекс
    return check(index + 1);
  }

  return check(0); // починаємо з індексу 0
}
