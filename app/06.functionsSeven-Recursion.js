function isPalindromeIterative2(s) {
  // 1. Уніфікуємо рядок: прибираємо пробіли та переводимо у нижній регістр
  let cleaned = "";
  for (let i = 0; i < s.length; i++) {
    if (s[i] !== " ") {
      cleaned += s[i].toLowerCase();
    }
  }

  // 2. Перевіряємо символи з обох боків
  for (
    let left = 0, right = cleaned.length - 1;
    left < right;
    left++, right--
  ) {
    if (cleaned[left] !== cleaned[right]) {
      return false; // якщо хоч одна пара не співпала → не паліндром
    }
  }

  // 3. Якщо всі символи співпали → паліндром
  return true;
}

