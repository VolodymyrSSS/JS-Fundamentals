/*
    Напишіть функцію з назвою findIndex, яка отримує рядок s та символ c і
    повертає індекс першого входження c у s. Якщо його не існує, повертається -1.
    Приклад: test, s -> 2 Оскільки символ s вперше з'являється з індексом 2.
    Покажіть 2 варіанти рішення один з яких використовує рекурсію.
*/
// Solution via iteration:
/*
    Алгоритмом рішення буде:
    1. Ми перебираємо всі символи рядка s.
    2. Якщо поточний символ збігається з c → одразу повертаємо його індекс.
    3. Якщо цикл закінчився і збігів не було → повертаємо -1.
*/
function findIndex1(s, c) {
  for (let i = 0; i < s.length; i++) {
    if (s[i] === c) {
      return i; // знайшли перше входження
    }
  }
  return -1; // якщо не знайдено
}
