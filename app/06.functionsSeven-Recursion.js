// Solution via memoized recursion:
function factorial2(n, memo = {}) {
  if (n in memo) return memo[n];
  if (n === 0 || n === 1) return 1;

  memo[n] = n * factorial2(n - 1, memo);
  return memo[n];
}
// Tests:
