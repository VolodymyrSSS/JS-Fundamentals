/*
    Напишіть функцію lcp, яка отримує масив рядків і повертає найдовший
    спільний префікс.
    Наприклад:
    ["somehow", "somewhat", "something", "some"] -> "some"
    ["maximum", "minimum"] -> "m"
    ["llo", "lloon"] -> "llo"
*/
// Solution via iterative
/*
    Алгоритм (ітеративний, простий):
    Беремо перший рядок як базу для порівняння.
    Перевіряємо кожен символ цього рядка з відповідним символом у всіх інших рядках.
    Якщо всі збігаються → додаємо символ до префіксу.
    Якщо хоч один не збігся → зупиняємось.
*/
function lcp(strings) {
  if (strings.length === 0) return ""; // якщо масив порожній

  let prefix = ""; // накопичуємо спільний префікс
  let first = strings[0]; // беремо перший рядок як базу

  // йдемо по символах першого рядка
  for (let i = 0; i < first.length; i++) {
    let char = first[i]; // поточний символ

    // перевіряємо цей символ у всіх інших рядках
    for (let j = 1; j < strings.length; j++) {
      // якщо рядок закінчився або символ не збігається
      if (i >= strings[j].length || strings[j][i] !== char) {
        return prefix; // зупиняємось і повертаємо результат
      }
      /*
        Це означає: якщо хоча б один рядок закінчився або символи не збігаються
        то зупиняйся, спільний префікс знайдено, бо:
        - рядок i >= strings[j].length означає - ми перебираємо символи першого 
        рядка (first). Якщо індекс i виходить за межі іншого рядка (strings[j]),
        це значить, що цей рядок закінчився раніше. Тоді спільний префікс не може
        бути довшим за цей рядок.
        - рядок strings[j][i] !== char означає - якщо символ у рядку strings[j] на 
        позиції i не збігається з символом char (який ми взяли з першого рядка),
        то спільний префікс закінчився.
      */
    }

    // якщо символ збігся у всіх рядках → додаємо його до префіксу
    prefix += char;
  }

  return prefix;
}
// Приклади
console.log(lcp(["somehow", "somewhat", "something", "some"])); // "some"
console.log(lcp(["maximum", "minimum"])); // "m"
console.log(lcp(["llo", "lloon"])); // "llo"
console.log(lcp(["abc", "def"])); // нічого - пустий рядок

// Solution via recursion
/*
    У рекурсивному варіанті ми теж рухаємось зліва направо символ за символом, але
    замість циклу використовуємо рекурсивні виклики check(index + 1).
*/
function lcpRecursive(strings) {
  if (strings.length === 0) return "";

  function check(index) {
    // якщо індекс виходить за межі будь-якого рядка → повертаємо префікс
    for (let j = 0; j < strings.length; j++) {
      if (index >= strings[j].length) {
        return strings[0].slice(0, index); //
      }
    }

    let char = strings[0][index];

    // якщо символ не збігається у будь-якому рядку → повертаємо префікс
    for (let j = 1; j < strings.length; j++) {
      if (strings[j][index] !== char) {
        return strings[0].slice(0, index);
      }
    }

    // якщо символ збігся → перевіряємо наступний
    return check(index + 1);
  }

  return check(0);
}
