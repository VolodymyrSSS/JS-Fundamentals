/* 
   Напишіть функцію fibo, яка отримує ціле число n та повертає n-й елемент
   послідовності Фібоначчі. Як відомо, у послідовності Фібоначчі 0-й
   елемент дорівнює 0, а 1-й елемент дорівнює 1. 
   Рішення покажіть із рекурсією, із мемоізацією та ітеруючи по ряду чисел.
   Тобто, треба створити функцію fibo, яка підраховув числа Фібоначчі по формулі
    F0 = 0, F1 = 1, Fn = Fn-1 + Fn-2. Використайте в рішенні Named Function 
    Expression (NFE).
*/
// Solution via recursion:
/*
   В основу рішення покладена послідовність Фібоначчі, яка починається з 0, 1, 
   і кожен елемент є сумою попередніх 2 елементів. Тобто послідовність на початку
   має вид:  0, 1, 1, 2, 3, 5, 8, 13, 21, ....
   Наприклад, 6-й елемент - це 8, тому що 3 + 5 = 8 (сума попередніх 2 елементів).
*/
const fibo1 = function fibonacci(n) {
  if (n === 0) {
    return 0;
  } else if (n === 1) {
    return 1;
  } else {
    return fibonacci(n - 1) + fibonacci(n - 2);
  }
};

console.log(fibo1(6)); // 8 (Число фібоначчі за індексом 6)
/*
   Є вагоме застереження: рекурсія елегантна, але неефективна для великих n,
   оскільки вона переобчислює багато значень. Наприклад, fibo(40) буде дуже
   повільною. На практиці для підвищення продуктивності доведеться використовувати
   мемоізацію або ітерацію.
*/

/*Ось мемоізований рекурсивний варіант функції Фібоначчі. Він зберігає елегантність 
   рекурсії, але уникає перерахунку тих самих значень знову і знову.*/

function fibo2(i, memo = {}) {
  if (i in memo) return memo[i]; // return cached result if available
  if (i === 0) return 0;
  if (i === 1) return 1;

  memo[i] = fibo2(i - 1, memo) + fibo2(i - 2, memo);
  return memo[i];
}

console.log(fibo2(40)); // 102334155

// Solution via iterative fibonacci: (не рекурсивний)
function fiboIterative(i) {
  if (i === 0) return 0;
  if (i === 1) return 1;

  let prev = 0,
    curr = 1;
  for (let n = 2; n <= i; n++) {
    const next = prev + curr;
    prev = curr;
    curr = next;
  }
  return curr;
}

