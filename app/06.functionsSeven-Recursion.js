/*
    Напишіть функцію isPalindrome, яка отримує рядок s та повертає true, якщо 
    s є паліндромом, інакше — false. Покажіть щонайменше 2 рішення: з використанням
    рекурсії та без.
 */
// Solution via Recursion:
/*
    Алгоритм рішення буде:
    1. Уніфікація рядка: видаляємо пробіли, переводимо у нижній регістр.
    2. Створюєм Функцію check(str):
    3. Якщо рядок порожній або з одного символу → це паліндром.
    4. Якщо перший і останній символи різні → повертаємо false.
    5. Якщо вони однакові → викликаємо check для наступних символів рядка але без
       першого і останнього символів.
    6. Рекурсія поступово «обрізає» рядок з обох боків, доки не залишиться середина:
        Приклад "1234321"
        Перевірка: 1 і 1 → співпали.
        Викликаємо check("23432").
        Перевірка: 2 і 2 → співпали.
        Викликаємо check("343").
        Перевірка: 3 і 3 → співпали.
        Викликаємо check("4").
        "4" має довжину 1 → базовий випадок → true.
*/
// Solution via Recursion (більш зрозумілий варіант)
function isPalindromeRecursive1(s) {
  // 1. Уніфікуємо рядок: прибираємо пробіли та переводимо у нижній регістр
  let cleaned = "";
  for (let i = 0; i < s.length; i++) {
    if (s[i] !== " ") {
      cleaned += s[i].toLowerCase();
    }
  }

  // 2. Рекурсивна функція, яка працює без індексів
  function check(str) {
    // базовий випадок: якщо довжина 0 або 1 → паліндром
    if (str.length <= 1) {
      return true;
    }

    // якщо перший і останній символи не співпадають → не паліндром
    if (str[0] !== str[str.length - 1]) {
      return false;
    }

    // рекурсивний виклик: перевіряємо наступні символи в середину рядка
    // ми «обрізаємо» крайні символи і залишаємо середину - тобто рухаємось до середини
    return check(str.slice(1, str.length - 1));
  }

  // 3. Запускаємо перевірку - рекурсивний виклик уніфікованого рядка
  return check(cleaned);
}
/*
    Отже, ми дивимось на перший і останній символи. Якщо вони однакові → відкидаємо їх
    і перевіряємо наступні символи (другий зліва і другий справа). Це робиться через 
    slice(1, str.length - 1) — воно створює новий рядок без першого і останнього символів.
    Рекурсія повторює цей процес, доки ми не дійдемо до середини. Якщо всі пари співпали 
    це → паліндром. Якщо хоч одна пара різна → не паліндром.
*/
// Приклади
console.log(isPalindromeRecursive1("А")); // true
console.log(isPalindromeRecursive1("ABBA")); // true
console.log(isPalindromeRecursive1("step on no pets")); // true

// Solution via Recursion: Рекурсія триває, доки не дійдемо до середини рядка.
function isPalindromeRecursive(s) {
  let cleaned = ""; // створюємо уніфікований рядок

  // перебираємо рядок посимвольно
  for (let i = 0; i < s.length; i++) {
    // якщо символ не є пробілом
    if (s[i] !== " ") {
      // приводимо до нижнього регістру і додаємо до уніфікованого рядка
      cleaned += s[i].toLowerCase();
    }
  }

  // рекурсивна допоміжна функція
  function check(left, right) {
    // left — індекс символу зліва (починається з 0)
    // right — індекс символу справа (починається з останнього символу)

    // базовий випадок: якщо індекси перетнулися або зрівнялися
    if (left >= right) {
      return true; // всі символи співпали
    }

    // якщо символи не співпадають → не паліндром
    if (cleaned[left] !== cleaned[right]) {
      return false;
    }

    // рекурсивний виклик: рухаємося до центру
    return check(left + 1, right - 1);
  }

  // тут ми вперше задаємо left і right:
  // left = 0 (перший символ), right = cleaned.length - 1 (останній символ)
  return check(0, cleaned.length - 1);
}

// Приклади
console.log(isPalindromeRecursive("121")); // true
console.log(isPalindromeRecursive("step on no pets")); // true
console.log(isPalindromeRecursive("Anna")); // true
console.log(isPalindromeRecursive("A")); // true

// Solution via Iterative version (loop instead of recursion)
function isPalindromeIterative(s) {
  let cleaned = ""; // створюємо уніфікований рядок

  // перебираємо рядок посимвольно
  for (let i = 0; i < s.length; i++) {
    // якщо символ не є пробілом
    if (s[i] !== " ") {
      // приводимо до нижнього регістру і додаємо до уніфікованого рядка
      cleaned += s[i].toLowerCase();
    }
  }

  // визначаємо індекси зліва та справа
  let left = 0;
  let right = cleaned.length - 1;

  // рухаємося з обох боків до центру
  while (left < right) {
    // якщо символи не співпадають → не паліндром
    if (cleaned[left] !== cleaned[right]) {
      return false;
    }
    left++; // беремо наступний символ зліва
    right--; // беремо попередній символ справа
  }

  // якщо всі символи співпали → паліндром
  return true;
}
// Tests:
console.log(isPalindromeIterative("1234321")); // true
console.log(isPalindromeIterative("bob")); // true
console.log(isPalindromeIterative("test")); // false

