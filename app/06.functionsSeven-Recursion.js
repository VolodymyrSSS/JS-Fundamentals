/*
    Напишіть функцію merge, яка отримує два відсортовані масиви цілих
    чисел і повертає об'єднаний масив цих двох відсортованих масивів.
    Приклади:
    [1,5,7], [2, 6, 9] -> [1, 2, 5, 6, 7, 9]
    [-1, 5], [0, 9] -> [-1, 0, 5, 9]
    Покажіть 2 варіанти рішення один з яких використовує рекурсію.
*/
// Solution via iteration:
/*
    Алгоритм рішення буде таким:
    Створюємо два індекси i і j, які починають з 0.
    Порівнюємо a1[i] і a2[j].
    Якщо a1[i] <= a2[j] → додаємо a1[i] у результат і рухаємо i.
    Інакше → додаємо a2[j] і рухаємо j.
    Повторюємо, доки один масив не закінчиться.
    Додаємо залишок іншого масиву.
*/
function merge1(a1, a2) {
  let result = [];
  let i = 0,
    j = 0;

  // поки є елементи в обох масивах
  while (i < a1.length && j < a2.length) {
    if (a1[i] <= a2[j]) {
      result.push(a1[i]);
      i++;
    } else {
      result.push(a2[j]);
      j++;
    }
  }

  // додаємо залишки
  while (i < a1.length) {
    result.push(a1[i]);
    i++;
  }

  while (j < a2.length) {
    result.push(a2[j]);
    j++;
  }

  return result;
}
// Tests:
console.log(merge1([1, 3, 5], [2, 4])); // [1, 2, 3, 4, 5]

// Solution via recursion:
/*
    На кожному кроці ми дивимось на перші елементи обох масивів.
    Беремо менший і додаємо його в результат.
    Потім рекурсивно викликаємо merge для 'залишків' масивів.
    Базовий випадок: якщо один масив порожній → повертаємо інший.
*/
function merge2(a1, a2) {
  // базові випадки
  if (a1.length === 0) return a2;
  if (a2.length === 0) return a1;

  // рекурсивний крок
  if (a1[0] <= a2[0]) {
    // беремо перший елемент з a1 і додаємо до результату
    return [a1[0]].concat(merge2(a1.slice(1), a2));
  } else {
    // беремо перший елемент з a2 і додаємо до результату
    return [a2[0]].concat(merge2(a1, a2.slice(1)));
  }
}
// Tests:
console.log(merge2([-3, 0, 12, 32, 54], [-3, -1, 1, 13, 48, 49, 50]));
// [-3, -3, -1, 0, 1, 12, 13, 32, 48, 49, 50, 54]
console.log(merge2([1, 2, 4], [2, 3])); // [1, 2, 2, 3, 4]
/*
     Це рекурсивне рішення показує «розкладання задачі на менші підзадачі»:
     кожен крок бере мінімальний елемент і делегує решту рекурсії. Ось
    покроково (для масивів [1,3,5] і [2,4]):

    Порівнюємо 1 і 2 → менший 1.
    Результат: [1] + merge([3,5], [2,4]).

    Порівнюємо 3 і 2 → менший 2.
    Результат: [1,2] + merge([3,5], [4]).

    Порівнюємо 3 і 4 → менший 3.
    Результат: [1,2,3] + merge([5], [4]).

    Порівнюємо 5 і 4 → менший 4.
    Результат: [1,2,3,4] + merge([5], []).

    Другий масив порожній → повертаємо [5].

    Остаточний результат: [1,2,3,4,5].
*/

